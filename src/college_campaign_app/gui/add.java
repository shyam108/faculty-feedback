    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        String candid=id.getText();
        String candname=name.getText();
         if(id.getText().isEmpty()||name.getText().isEmpty())
        {
        JOptionPane.showMessageDialog(null,"ENTET ID OR NAME FIRST", "Error", JOptionPane.ERROR_MESSAGE);
        }
        else
         {
             Candidates cd=new Candidates(candid,candname);
             try{
         boolean result=Candidatemethod.addCandidateDetails(cd);
         if(result==true)
         {
            
               JOptionPane.showMessageDialog(null,"successfully record dd", "success", JOptionPane.INFORMATION_MESSAGE);
               this.dispose();
                    adminoptionafterlogin aoal=new adminoptionafterlogin();
                    aoal.setVisible(true);
         }
           else
           {
               JOptionPane.showMessageDialog(null,"failed to insert", "failed", JOptionPane.WARNING_MESSAGE);
           }
         
             }
             catch(Exception e)
             {
                 JOptionPane.showMessageDialog(null,"NOT CONNECTED", "ERROR", JOptionPane.WARNING_MESSAGE);
             }
             }
    }//GEN-LAST:event_jButton1ActionPerformed

